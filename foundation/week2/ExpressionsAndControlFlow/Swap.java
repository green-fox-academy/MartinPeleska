package com.company;

public class Swap {
    public static void main(String[] args) {
        // Swap the values of the variables

        int a, b, c;
        a = 123;
        b = 526;

        System.out.println(a);
        System.out.println(b);

        c = a;
        a = b;
        b = c;

        System.out.println(a);
        System.out.println(b);


    }
}
